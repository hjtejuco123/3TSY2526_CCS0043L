<?php
//Check if Array
$data = array(1, 2, 3);
if (is_array($data)) {
    echo "Data is an array.";
}
?>


<?php
//Check if String
$text = "Hello";
if (is_string($text)) {
    echo "Text is a string.";
}
?>

<?php
//Check if Numeric
$number = "123";
if (is_numeric($number)) {
    echo "Number is numeric.";
}
?>

<?php
//Check if Null
$value = null;
if (is_null($value)) {
    echo "Value is null.";
}
?>